package com.aikya.rnsit.crisisoninfiniteearths;

public class CodeAndImage {
    private String code;
    private int image;

    public CodeAndImage(String code, int image) {
        this.code = code;
        this.image = image;
    }

    public String getCode() {
        return code;
    }

    public int getImage() {
        return image;
    }
}
